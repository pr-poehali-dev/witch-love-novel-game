export type StyleType = 'mystic' | 'dark' | 'playful';

export type Tone = 'cold' | 'warm' | 'shy' | 'love';

export function toneOf(love: number): Tone {
  if (love >= 70) return 'love';
  if (love >= 50) return 'shy';
  if (love >= 20) return 'warm';
  return 'cold';
}

